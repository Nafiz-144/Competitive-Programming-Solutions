#include <bits/stdc++.h>
using namespace std;
int main()
{
    long long a, b, c, d, e;
    cin >> a >> b;
    c = a + b;
    e = a * b;
    d = a - b;

    cout << a << " + " << b << " = " << c << endl;
    cout << a << " * " << b << " = " << e << endl;
    cout << a << " - " << b << " = " << d << endl;

    return 0;
}